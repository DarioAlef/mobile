fun main() {
    // estrutura de repetição - for
    for (i in 1..10) {
        println(i)
    }

    // estrutura de repetição - while
    var i = 0
    while (i < 10) {
        println(i)
        i++
    }

    // estrutura de repetição - do while
    var j = 0
    do {
        println(j)
        j++
    } while (j < 10)
}