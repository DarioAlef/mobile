// 4. Faça um programa que leia 3 valores em variáveis distintas, armazena a soma das 
// duas primeiras em uma nova variável e o produto das duas últimas em outra. Mostre, 
// como resultado, o produto das duas novas variáveis.

fun main() {
    val a = 5
    val b = 10
    val c = 3

    val soma = a + b
    val produto = b * c

    println("O produto das duas novas variáveis é: ${soma * produto}")
}